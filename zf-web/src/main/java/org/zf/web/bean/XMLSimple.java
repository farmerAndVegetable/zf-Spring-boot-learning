package org.zf.web.bean;

public class XMLSimple {
	private String id;
	private int age;
	private String name;
	private XmlSonClass xmlSonClass;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public XmlSonClass getXmlSonClass() {
		return xmlSonClass;
	}
	public void setXmlSonClass(XmlSonClass xmlSonClass) {
		this.xmlSonClass = xmlSonClass;
	}
	

}
